      <p>61. <a href="document.php?id=APB1071.xml">Stephen F. Austin to Governor Rafael
      Gonzales, 04-04-1825</a>:</p>

      <p>Austin to Gov. Gonzales, April 4, 1825. First contract for 300 families filled.
      Three hundred more anxious to come. Many petitions from Louisiana, Mississippi, and
      Alabama. Some already arrived, and some on the road. Has expelled some bad
      characters from the colony, and they have spread damaging reports about the colony.
      Government will not be deceived. Petition for permit to settle 500 additional
      families. Argument: (1) The 300 families already settled too weak for adequate
      defense against Indians&#8212;need reinforcements; (2) vacant lands between
      settlers in his colony should be distributed by himself to avoid conflicting
      claims; (3) if the port of Galveston is opened, the settlers can not get full
      advantage from it without additional colonists; (4) the country can only be
      enriched by foreign trade, and must have more population in order to produce enough
      commerce to attract foreigners. Suggests that new settlers be allowed to bring
      slaves and keep them and their descendants as slaves. Otherwise the immigrants will
      be confined to the poorest class and development of the province will be much
      delayed.</p><br />

